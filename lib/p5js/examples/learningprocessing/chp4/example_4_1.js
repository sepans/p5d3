// Example 4-1: Variable declaration and initialization examples
var count = 0;        // Declare an variable named count, assigned the value 0
var letter = 'a';     // Declare a variable named letter, assigned the value 'a'
var d = 132.32;      	// Declare a variable named d, assigned the value 132.32
var happy = false;	  // Declare a variable named happy, assigned the value false
var x = 4.0;          // Declare a variable named x, assigned the value 4.0
var y;                // Declare a variable named y (no assignment)
y = x + 5.2;          // Assign the value of x plus 5.2 to the previously declared y
var z = x*y + 15.0;   // Declare a variable named z, assign it the value which 
   		        				// is x times y plus 15.0.


